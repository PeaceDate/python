# Numbers and arithmetic operations
if __name__ == '__main__':
    print(-2000) # Взятие обратного числа
    print(2000 ** 2) # Возведение в степень
    print(2000 // 2) # Целочисленное деление
    print(2000 % 2) # Остаток от деления
    print(2000 / 2) # Деление
    print(2000 * 2) # Умножение
    print(2000 + 2) # Сложение
    print(2000 - 2) # Вычитание